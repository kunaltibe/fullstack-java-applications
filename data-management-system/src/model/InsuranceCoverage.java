public class InsuranceCoverage {
    private String corporateID;
    private String corporateAdd;
    private String premium;
    private String deductable;
    private String planName;

    public String getPlanName() {
        return planName;
    }

    public void setPlanName(String planName) {
        this.planName = planName;
    }

    public String getCorporateID() {
        return corporateID;
    }

    public void setCorporateID(String corporateID) {
        this.corporateID = corporateID;
    }

    public String getCorporateAdd() {
        return corporateAdd;
    }

    public void setCorporateAdd(String corporateAdd) {
        this.corporateAdd = corporateAdd;
    }

    public String getPremium() {
        return premium;
    }

    public void setPremium(String premium) {
        this.premium = premium;
    }

    public String getDeductable() {
        return deductable;
    }

    public void setDeductable(String deductable) {
        this.deductable = deductable;
    }

    
    
            
}
