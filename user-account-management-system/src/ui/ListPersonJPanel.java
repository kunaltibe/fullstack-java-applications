    private void populatePersonTable() {
        DefaultTableModel model = (DefaultTableModel) tblListPerson.getModel();
         model.setRowCount(0);
         
         for(Person person : personhistory.getPersonList()){
             Object[] col = new Object[3];
             col[0] = person;
             col[1] = person.getAddress();
             col[2] = person.getContact();
             
             model.addRow(col);
        }   
    }
    
