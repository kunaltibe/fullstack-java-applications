    private void btnViewPersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewPersonActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblListPerson.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this,"Please select a row to view Person details.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblListPerson.getModel();
        Person selectedPerson = (Person) model.getValueAt(selectedRow, 0);
        txtName.setText(selectedPerson.getName());
        txtAddress.setText(selectedPerson.getAddress());
        txtContact.setText(String.valueOf(selectedPerson.getContact()));
        txtEmailid.setText(selectedPerson.getEmail());       
    }//GEN-LAST:event_btnViewPersonActionPerformed

