    private void populateUserTable() {
        DefaultTableModel model = (DefaultTableModel) tblListUser.getModel();
        model.setRowCount(0);

        for(User user : userhistory.getUserList()){
            Object[] col = new Object[3];
            col[0] = user;
            col[1] = user.getUsername();
            model.addRow(col);
        }
    }
