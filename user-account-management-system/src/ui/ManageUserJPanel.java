    private void btnViewUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewUserActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblListUser.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(this,"Please select a row to view User details.");
            return;
        }
        User selectedUser = (User) tblListUser.getValueAt(selectedRow, 0);
        ListUserJPanel panel = new ListUserJPanel(workAreaPanel, selectedUser);
        workAreaPanel.add("ListUserJPanel", panel);
        CardLayout layout = (CardLayout) workAreaPanel.getLayout();
        layout.next(workAreaPanel);
    }//GEN-LAST:event_btnViewUserActionPerformed

