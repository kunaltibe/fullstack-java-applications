    private void btnBlockUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBlockUserActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblListUser.getSelectedRow();
        
        if(selectedRow>=0){
            User selectedUser = (User) tblListUser.getValueAt(selectedRow, 0);
            userhistory.blockUser(selectedUser);
            JOptionPane.showMessageDialog(this, "User is blocked!");
            populateUserTable();
            
        }else{
            JOptionPane.showMessageDialog(this,"Please select a row to block User.");
        }
        
        
    }//GEN-LAST:event_btnBlockUserActionPerformed


